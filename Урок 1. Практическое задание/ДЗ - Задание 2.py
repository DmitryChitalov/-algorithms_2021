# Считаем наименьшим 1-ый эл-т. Отсортируем список от меньшего к большему.
# Если в процессе сортировки находится эл-т меньше 1-го, то они меняются местами.
def list_sort(num):
    # Цикл перебора отсортированных индексов значений списка
    for i in range(len(num)):# !!! O(n)
        lowest_value_index = i # Присваиваем наименьшему первому элементу индекс i # !!! O(1)
        # Цикл перебирает остальные (несортированные) элементы начиная с i + 1
        for j in range(i + 1, len(num)): # !!! O(n)
            if num[j] < num[lowest_value_index]:# Если для эл-та с индексом j это верно, то # !!! O(1)
                lowest_value_index = j # Говорич, что наименьший элемент, несортированной части, имеет индекс j # !!! O(1)
        # Имеем вложенный цикл со сложностью O(n) в цикл O(n), т.е. имеем сл-ть O(n^2) - ДОМИНАНТ
        # Этот наименьший элемент меняем с первым в списке
        num[i], num[lowest_value_index] = num[lowest_value_index], num[i]# !!! O(1)
# !!! Имеем 2 вложенных линейных O(n) цикла
num = [12, 8, 3, 20, 11]
list_sort(num)
print(num)
