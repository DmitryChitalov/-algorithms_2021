"""
Второй, в виде функции, должен обеспечивать поиск минимального значения для списка.
Сложность такого алгоритма: O(n) - линейная.

Примечание:
Прошу вас внимательно читать ТЗ и не забыть выполнить все пункты.
"""

def list_min(num_1):
    smallest = num_1[0] # Говорим, что певый эл-т списка минимальный #!!! O(1)
# Ищем наименьшее значение
    for i in num_1: #!!! O(n) - ДОМИНАНТ
        if i < smallest:#!!! O(1)
            smallest = i#!!! O(1)
    return smallest#!!! O(1)
num_1 = [48, 52, 23, 41, 32]
print("Наименьший эл-т списка равен: ",  list_min(num_1))




