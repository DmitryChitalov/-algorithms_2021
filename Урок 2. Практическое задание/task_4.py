# Дальше не буду проверки валидности ввода делать. Они такие же, как в предыдущих заданиях.


def row_sum(val):
    if val == 1:
        return val
    return (-0.5) ** (val - 1) + row_sum(val - 1)


value = int(input('Введите количество элементов: '))

print(f'Количество элементов: {value}, их сумма: {row_sum(value)}')
