import sys


def sum_el(number):
    global num
    if number == num:       # базовый случай
        return (1/(2**(number - 1)))*((-1)**(number - 1))
    return (1/(2**(number - 1)))*((-1)**(number - 1)) + sum_el(number + 1)   # шаг рекурсии


