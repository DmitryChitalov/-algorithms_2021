def sum_of_geom_pr(n=1, a=1.0, s=-0.5, my_sum=1.0):
    if n == 1:
        print(f'Искомая сумма: {my_sum}')
        return
    else:
        sum_of_geom_pr(n - 1, a=a * s, my_sum=my_sum + a * s)


