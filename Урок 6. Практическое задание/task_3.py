from random import randint
from memory_profiler import profile


@profile
def decor(func):
    def wrapper(*args):
        return func(*args)

    return wrapper


@decor
def recursive_reverse(number):
    if number == 0:
        return ''
    return f'{str(number % 10)}{recursive_reverse(number // 10)}'


num_100 = randint(10000, 1000000)

recursive_reverse(num_100)
