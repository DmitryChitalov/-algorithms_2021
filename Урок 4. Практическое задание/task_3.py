from timeit import timeit
from cProfile import run



def revers_1(enter_num, revers_num=0):
    if enter_num == 0:
        return revers_num
    else:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
        return revers_1(enter_num, revers_num)


def revers_2(enter_num, revers_num=0):
    while enter_num != 0:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
    return revers_num


def revers_3(enter_num):
    enter_num = str(enter_num)
    revers_num = enter_num[::-1]
    return revers_num


def revers_4(enter_num):
    enter_num_list = list(enter_num)
    enter_num_list.reverse()
    return ''.join(enter_num_list)


enter_num = 18556834


print('revers_1:', timeit('revers_1(enter_num)', globals=globals()))
print('revers_2:', timeit('revers_2(enter_num)', globals=globals()))
print('revers_3:', timeit('revers_3(enter_num)', globals=globals()))
print('revers_4:', timeit('revers_4(enter_num)', globals=globals()))


def main():
    revers_1(enter_num)
    revers_2(enter_num)
    revers_3(enter_num)
    revers_4(enter_num)


print()
run('main()')

