from timeit import timeit
from cProfile import run


def revers_1(enter_num, revers_num=0):
    if enter_num == 0:
        return revers_num
    else:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
        return revers_1(enter_num, revers_num)


def revers_2(enter_num, revers_num=0):
    while enter_num != 0:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
    return revers_num


def revers_3(enter_num):
    enter_num = str(enter_num)
    revers_num = enter_num[::-1]
    return revers_num


num = 1234567893475857349857348957
print(f'Функция_1: {timeit("revers_1(num)", globals=globals())}')
print(f'Функция_2: {timeit("revers_2(num)", globals=globals())}')
print(f'Функция_3: {timeit("revers_3(num)", globals=globals())}')
print('=' * 100)
run("revers_1(num)")
print('=' * 100)
run("revers_2(num)")
print('=' * 100)
